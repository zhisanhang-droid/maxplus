let currentLanguage = "en";

const navToggle = document.querySelector(".nav-toggle");
const navMenu = document.querySelector(".nav__menu");
const langButtons = Array.from(document.querySelectorAll("[data-lang-button]"));
const metaDescription = document.querySelector('meta[name="description"]');

const applyLanguage = (lang) => {
  currentLanguage = translations[lang] ? lang : "en";
  const copy = translations[currentLanguage];

  document.documentElement.lang = currentLanguage === "zh" ? "zh-CN" : "en";
  document.title = copy.metaTitle;
  metaDescription?.setAttribute("content", copy.metaDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (copy[key] !== undefined) {
      element.textContent = copy[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (copy[key] !== undefined) {
      element.setAttribute("placeholder", copy[key]);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (copy[key] !== undefined) {
      element.setAttribute("aria-label", copy[key]);
    }
  });

  document.querySelectorAll(".form-status").forEach((status) => {
    if (status.textContent.trim()) {
      status.textContent = copy.formDemoStatus;
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langButton === currentLanguage);
  });

  window.localStorage.setItem("maxplus-sporting-lang", currentLanguage);
};

if (navToggle && navMenu) {
  navToggle.addEventListener("click", () => {
    const isOpen = navMenu.classList.toggle("is-open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navMenu.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      navMenu.classList.remove("is-open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langButton);
  });
});

const savedLanguage = window.localStorage.getItem("maxplus-sporting-lang") || "en";
applyLanguage(savedLanguage);

const slider = document.querySelector("[data-slider]");

if (slider) {
  const slides = Array.from(slider.querySelectorAll(".hero-slide"));
  const dots = Array.from(slider.querySelectorAll(".hero-dot"));
  let activeIndex = 0;
  let autoplayId;

  const setSlide = (index) => {
    slides.forEach((slide, slideIndex) => {
      slide.classList.toggle("is-active", slideIndex === index);
    });

    dots.forEach((dot, dotIndex) => {
      dot.classList.toggle("is-active", dotIndex === index);
    });

    activeIndex = index;
  };

  const nextSlide = () => {
    setSlide((activeIndex + 1) % slides.length);
  };

  const restartAutoplay = () => {
    window.clearInterval(autoplayId);
    autoplayId = window.setInterval(nextSlide, 5200);
  };

  dots.forEach((dot, index) => {
    dot.addEventListener("click", () => {
      setSlide(index);
      restartAutoplay();
    });
  });

  setSlide(0);
  restartAutoplay();
}

const widget = document.querySelector("[data-subscribe-widget]");

if (widget) {
  const toggle = widget.querySelector(".subscribe-widget__toggle");

  toggle?.addEventListener("click", () => {
    const isOpen = widget.classList.toggle("is-open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });
}

document.querySelectorAll("[data-demo-form]").forEach((form) => {
  const status = form.querySelector(".form-status");

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    if (status) {
      status.textContent = translations[currentLanguage].formDemoStatus;
    }
    form.reset();
  });
});

const revealItems = document.querySelectorAll(".reveal");

if ("IntersectionObserver" in window && revealItems.length) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealItems.forEach((item) => observer.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}
